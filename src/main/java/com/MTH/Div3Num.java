package com.MTH;

public class Div3Num {
        public static void main(String[] args) {
            int num1 = 23;
            int num2 = 11;
            int num3 = 77;

            // Calculate average
            double average = (num1 + num2 + num3) / 3.0;

            // Print result
            System.out.println("(" + num1 + " + " + num2 + " + " + num3 + ") / 3 = " + average);
        }
    }
